    // Large mean target precision, so `prec` frequently exceeds EXP_THRESHOLD (25000) and the
    // binary-splitting `exp_3` path is exercised; a larger input precision drives its general
    // (chunk) loop.
    let mut config = GenConfig::new();
    config.insert("mean_small_n", 50000);
    config.insert("mean_precision_n", 256);
