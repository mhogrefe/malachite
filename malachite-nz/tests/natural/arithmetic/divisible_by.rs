fn verify_limbs_divisible_by(ns: &[Limb], ds: &[Limb], divisible: bool) {
    assert_eq!(
        Natural::from_limbs_asc(ns).divisible_by(Natural::from_limbs_asc(ds)),
        divisible
    );
}

